#ifndef _TASK_IO_READ_H
#define _TASK_IO_READ_H

#include "main.h"

/*******************************************************************************************************/
/* IO输入状态结构体 - 供外部任务直接读取 */

typedef struct {
    uint8_t io_wake_up; /* 上电唤醒信号 (0: 未唤醒, 1: 已唤醒) */
    uint8_t io_fault00; /* 故障信号 (0: 正常, 1: 故障) */
    /* 后续可在此扩展其他IO输入信号 */
} IO_State_t;

/*******************************************************************************************************/
/* 外部变量声明 */

extern volatile IO_State_t g_io_state; /* 全局IO输入状态变量，volatile防止编译器优化 */

/*******************************************************************************************************/
/* 任务函数声明 */

void Task_IO_Read(void *argument); /* IO输入读取任务 (50ms周期) */

#endif // _TASK_IO_READ_H
